package Entity;

/**
 *
 * @author besnainou
 */
public class VisiteurBis {
   private int id;
   private String nom;
   
   public VisiteurBis(int unId,String unNom){
       id=unId;
       nom=unNom;
   }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the nom
     */
    public String getNom() {
        return nom;
    }

    /**
     * @param nom the nom to set
     */
    public void setNom(String nom) {
        this.nom = nom;
    }
}
